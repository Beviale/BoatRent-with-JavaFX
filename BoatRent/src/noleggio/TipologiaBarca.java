/**
 * <p>Specifica il tipo di barca </p>
 * <p>1. Base: non possiede ne' bagno ne' cabine.</p>
 * <p>2. Medio: possiede un solo bagno e nessuna cabina. </p>
 * <p>3. Avanzato: possiede due bagni e una cabina. </p> 
 */
public enum TipologiaBarca 
{
	Medio,
	Base,
	Avanzato
}
